package com.libit.wingspayroll.Model;

public class SendAttendenceModel {

    String
            Employee_number,
            Employee_Name,
            Attendence_Photo,
            Location,
            Empid,
            EmpCode;

    public String getEmployee_number() {
        return Employee_number;
    }

    public void setEmployee_number(String employee_number) {
        Employee_number = employee_number;
    }

    public String getEmployee_Name() {
        return Employee_Name;
    }

    public void setEmployee_Name(String employee_Name) {
        Employee_Name = employee_Name;
    }

    public String getAttendence_Photo() {
        return Attendence_Photo;
    }

    public void setAttendence_Photo(String attendence_Photo) {
        Attendence_Photo = attendence_Photo;
    }

    public String getLocation() {
        return Location;
    }

    public void setLocation(String location) {
        Location = location;
    }

    public String getEmpid() {
        return Empid;
    }

    public void setEmpid(String empid) {
        Empid = empid;
    }

    public String getEmpCode() {
        return EmpCode;
    }

    public void setEmpCode(String empCode) {
        EmpCode = empCode;
    }

}


